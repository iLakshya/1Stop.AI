# if condition example
sun = 'down'
if sun == 'down' :
    print("good night")
print("End of program 1")

sunn = 'not down'
if sunn == 'down':
    print("good morning")
print("End of program 2")

# else condition example
sunnn = 'down'
if sunnn == 'down':
    print("good evening")
else:
    print("good night")
print("End of program 3")

